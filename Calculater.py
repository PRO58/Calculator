class Calculater:
	def __init__(self, num1, num2):
		self.num1 = num1
		self.num2 = num2 



	def Subtraction(self):
		return self.num1 - self.num1



	def Multiplication(self):
		return self.num1 * self.num1


	def Addition(self):
		return float(self.num1 + self.num2)


	
	def Division(self):
		return self.num1 / self.num2

	



